# Introduction to Python: Understanding Expressions and Statements

# Variables and Assignment:
# In Python, we use variables to store and manage values.
# Example:
my_variable = 10  # Here, 'my_variable' is a variable, and we assign the value 10 to it.

# Expressions:
# An expression is a piece of code that produces a value. It can be as simple as a single value or a combination of values, variables, and operators.
# Example expressions:
result = my_variable + 5  # Adds 5 to the value of 'my_variable', and 'result' becomes the result.
message = "Hello" + " " + "World"  # Combines three strings to create a new string.

# Statements:
# A statement is a complete line of code that performs an action. Assigning a value to a variable is a statement, and it ends with a newline.
# Example statements:
my_variable = 20  # Assigns the value 20 to the variable 'my_variable'.
print("Hello, Python!")  # Prints the specified message to the console.

# Combining Expressions and Statements:
# You can use expressions within statements to perform more complex actions.
# Example:
new_result = my_variable * 2 + 3  # Uses an expression to calculate a result and assigns it to a new variable.

# Summary:
# - Variables hold values.
# - Expressions produce values through combinations of variables, values, and operators.
# - Statements perform actions, such as assigning values to variables or printing messages.